	HAL_ADCEx_Calibration_Start(&hadc1);
	HAL_ADC_Start(&hadc1);
	
	int air_tempt = 25;
	float fan_speed_pctg = 0; // 0 to 100
	int fan_op_mode = 0; // off, auto, manual
	int silent_enabled = 0; // off, on
	
